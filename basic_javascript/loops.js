// for loop:

for(let i=0; i<5; i++){
    console.log(i)
}

// while loop:

let i = 0

while(i<5){
    console.log(i)
    i++
}


// do while loop:

let j = 0

do{
    console.log(j)
    j++
}while(j<5)



